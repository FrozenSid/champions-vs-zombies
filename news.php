<div id="newsheader">
2. Tournaments
</div>

<div class="tournaments">
At the end of each month there will be a tournament between the players of 3 servers. There will be 3 rounds
to compete in and each round will have a time limit of one map. From the first round, the top 5 players will advance
from each server meaning those players that had the most zombie kills and least zombie infections after
the map time ends. The top 3 players of each server will advance to the second round. Finally, the winners
of the tournament will be the top 3 players from the second round. Each month the top 3 players will be
posted in our forum but that is not all. There will be prices for those players as well. First place gets
administrator priviledges in all of our servers for two months. Second place gets a $20 gift card. Third
place gets nickname protection forever meaning no one can log in the servers with his nickname. 
More information regarding the tournaments will be posted in our forum.
</div>





