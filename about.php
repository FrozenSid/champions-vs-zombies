<div class="about"> 
<span style= "color: #00FFEE;"> "Champions VS Zombies" </span> is an online multiplayer PC game
where champions and zombies are in constant battle. The game involes a lot of action and also
requires a lot of skills to be victorious. Online servers can be found once you run tha game and 
go to the Internet tab since the game can also be played locally within a local area network. 
This can be done by clicking on the LAN tab but you need to have a lot of people in that area since
it will not be fun playing with just a bunch of people. There are ten originally launched servers
which will be online 24/7. Each server has a capacity of 30 people to play in it. If a server is full, 
you will not be able to enter until a free spot is open.
</br>
</br>
</br>
<span style= "color: #00FFEE;"> 
The fun part about the game is that you can make your own server! Yes, there is a console that allows you to
create your server based on your IP address but you need to have a static IP address. If you want your server
to be up 24/7 you can simply not turn off your computer or pay for one of our hosting providers and the server's IP
address will be based on that provider. You can build a server with a capacity of either 15, 20 or 30 people in it.
You can also select the starting map and include the maps you want your server to rotate.
</span>

</br>
</br>
</br>
Before you enter the game the server will ask you to select a Champion from all the Champions available in the game.
Once each player has done that the round starts. The server counts to 10 and selects a player which will be infected
as the first zombie. The goal of that zombie is to infect the rest of the champions by touching them. If the Champions
kill the zombie they win the round, but if the zombie infects other champions the round continues until all zombies die
or all champions get infected. Once a zombie dies it resurrects so the more zombies there are, the more difficult it will
be to kill them all.
</br>
</br>
</br>
<span style= "color: #00FFEE;"> 
Each map is 40 minutes long with 4:00 minutes per round making that 10 rounds per map before the server changes
the game to the next map. Each round a Champion player starts with his original pistol, gun and special ability.
If he wants to buy another Champion's pistol or gun, he can do that through the game menu by pressing "B". Once he
buys another gun or pistol, he loses his already owned weapons but the special ability is not lost. Each champion gets
$500 per zombie kill. A player can also change his Champion if he wants but the effects will take place the next round.
This can be done from the game menu by pressing "M + 1" and a number from 0 to 9 selecting the specific Champion with 0
being represented as the number 10.
</span>

</br>
</br>
</br>
If a player is infected as a Zombie, he could switch his zombie character as well and the effects will take place the
next round. This can also be done from the game menu by pressing "M + 2" and a number from 0 to 9 selecting the
specific Zombie with 0 being represented as the number 10.
</br>
</br>
</br>
<span style= "color: #00FFEE;"> 
It is better to survive as a Champion because the more kills you get, you are likely to be in the "Top 10" players of
the server. The "Top 10" is a ranking of each server based on the number of kills you get as a champion and the number of
deaths you get as a zombie. So the more kills and the less deaths, the likelihood for being in the "Top 10" is much
higher. If you get 70 kills and 10 deaths, this will definetely put you at a spot in the ranking depending on how good
the other players are. Every 2 months "Top 10" resets the ranks and you will have a chance to prove yourself again.
</span>


